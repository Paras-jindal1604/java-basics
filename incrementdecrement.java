public class incrementdecrement {
    public static void main(String[] args) {
        int x = 9;
        int y = 9;
        int z = 9;
        int t = 9;

        System.out.println(x);
        System.out.println(x++);
        System.out.println(x);

        System.out.println();

        System.out.println(y);
        System.out.println(++y);
        System.out.println(y);
        
        System.out.println();

        System.out.println(z);
        System.out.println(z--);
        System.out.println(z);

        System.out.println();
        
        System.out.println(t);
        System.out.println(--t);
        System.out.println(t);

    }
    
}
